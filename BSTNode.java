public class BSTNode {
	private int key;
	private BSTNode left;
	private BSTNode right;
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public BSTNode getLeft() {
		return left;
	}
	public void setLeft(BSTNode left) {
		this.left = left;
	}
	public BSTNode getRight() {
		return right;
	}
	public void setRight(BSTNode right) {
		this.right = right;
	}
}
